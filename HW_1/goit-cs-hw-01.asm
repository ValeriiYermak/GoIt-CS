org 0x100        ; Початок COM-файлу (для DOS)

section .data
    a db 5        ; Значення змінної a
    b db 5       ; Значення змінної b
    c db 3        ; Значення змінної c
    result db 0   ; Змінна для зберігання результату
    msg db 'Result: $' ; Повідомлення для виведення

section .text

_start:
    ; Завантажуємо значення b у регістр AL
    mov al, [b]

    ; Віднімаємо значення c від AL (AL = b - c)
    sub al, [c]

    ; Додаємо значення a до AL (AL = b - c + a)
    add al, [a]

    ; Зберігаємо результат у змінну result
    mov [result], al

    ; Виведення результату на екран
    ; Для цього перетворимо число у ASCII-код
    add al, '0'
    mov [result], al

    ; Виводимо повідомлення "Result: "
    mov ah, 0x09        ; Функція DOS для виведення рядка
    mov dx, msg         ; Адреса рядка для виведення
    int 0x21            ; Виклик переривання DOS

    ; Виводимо результат (один символ)
    mov ah, 0x02        ; Функція DOS для виведення символу
    mov dl, [result]    ; Символ для виведення
    int 0x21            ; Виклик переривання DOS

    ; Завершення програми
    mov ah, 0x4C        ; Функція DOS для завершення програми
    int 0x21            ; Виклик переривання DOS